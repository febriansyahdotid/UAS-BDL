    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        BasicDBObject indexhapus = new BasicDBObject();
        indexhapus.put("idPegawai", textidP.getText());
        
        tbl.setValueAt(textidP.getText(), baris, 0);
        tbl.setValueAt(textnamaP.getText(), baris, 1);
        tbl.setValueAt(textuserP.getText(), baris, 2);
        tbl.setValueAt(textpassP.getText(), baris, 3);
        tbl.setValueAt(textalamatP.getText(), baris, 4);
        tbl.setValueAt(textnotelpP.getText(), baris, 5);
        
        collection.remove(indexhapus);
        tbl.removeRow(baris);
        reset();
    }//GEN-LAST:event_btndeleteActionPerformed

    private void tblpegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpegawaiMouseClicked
        // TODO add your handling code here:
        
        int baris = tblpegawai.rowAtPoint(evt.getPoint());
        String id = tblpegawai.getValueAt(baris, 0).toString();
        textidP.setText(id);
        String nama = tblpegawai.getValueAt(baris, 1).toString();
        textnamaP.setText(nama);
        String user = tblpegawai.getValueAt(baris, 2).toString();
        textuserP.setText(user);
        String pass = tblpegawai.getValueAt(baris, 3).toString();
        textpassP.setText(pass);
        String alamat = tblpegawai.getValueAt(baris, 4).toString();
        textalamatP.setText(alamat);
        String notelp = tblpegawai.getValueAt(baris, 5).toString();
        textnotelpP.setText(notelp);
    }//GEN-LAST:event_tblpegawaiMouseClicked

