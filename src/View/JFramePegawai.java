    private void load_table(){
        DefaultTableModel model = (DefaultTableModel) tblpegawai.getModel();
        while(cursor.hasNext()){
            DBObject obj = cursor.next();
            String textidP = (String)obj.get("idPegawai");
            String textnamaP = (String)obj.get("namaPegawai");
            String textuserP = (String)obj.get("userPegawai");
            String textpassP = (String)obj.get("passPegawai");
            String textalamatP = (String)obj.get("alamatPegawai");
            String textnotelpP = (String)obj.get("notelpPegawai");
            
            model.addRow(new Object[]{
                textidP, textnamaP, textuserP, textpassP, textalamatP, textnotelpP

            });
        }
        tblpegawai.setModel(model);
    }
