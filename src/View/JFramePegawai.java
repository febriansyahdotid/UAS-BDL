        add.put("userPegawai", textuserP.getText());
        add.put("passPegawai", textpassP.getText());
        add.put("alamatPegawai", textalamatP.getText());
        add.put("notelpPegawai", textnotelpP.getText());
        
        collection.insert(add);
        
        tbl.addRow(new Object[] {
        textidP.getText(), textnamaP.getText(), textuserP.getText(), textpassP.getText(), textalamatP.getText(), textnotelpP.getText()
    });
