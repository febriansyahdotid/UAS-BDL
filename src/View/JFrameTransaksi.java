    private void load_table(){
        DefaultTableModel model = (DefaultTableModel) tbltransaksi.getModel();
        while(cursor.hasNext()){
            DBObject obj = cursor.next();
        
            String textidPemesan = (String)obj.get("idPemesan");
            String textnamaPegawai = (String)obj.get("namaPegawai");
            String textnamaCustomer = (String)obj.get("namaCustomer");
            String textmakanan = (String)obj.get("namaMakanan");
            String textqty = (String)obj.get("qty");
            String textminuman = (String)obj.get("namaMinuman");
            String textqtyy = (String)obj.get("qtyy");
            String texttotal = (String)obj.get("totalHarga");
            
            model.addRow(new Object[] {
                textidPemesan, textnamaPegawai, textnamaCustomer, textmakanan, textqty,
                textminuman, textqtyy, texttotal
            });
        }
        tbltransaksi.setModel(model);
    }

    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        
        BasicDBObject add = new BasicDBObject();
        add.put("idPemesan", textidPemesan.getText());
        add.put("namaPegawai", textnamaPegawai.getText());
        add.put("namaCustomer", textnamaCustomer.getText());
        add.put("namaMakanan", textmakanan.getText());
        add.put("qty", textqty.getText());
        add.put("namaMinuman", textminuman.getText());
        add.put("qtyy", textqtyy.getText());
        add.put("totalHarga", texttotal.getText());
        
        collection.insert(add);

        tbl.addRow(new Object[] {
            textidPemesan.getText(), textnamaPegawai.getText(), textnamaCustomer.getText(), textmakanan.getText(), textqty.getText(),
                textminuman.getText(), textqtyy.getText(), texttotal.getText()
    });
        tbltransaksi.setModel(tbl);
        textidPemesan.setText("");
        textnamaPegawai.setText("");
        textnamaCustomer.setText("");
        textmakanan.setText("");
        textqty.setText("");
        textminuman.setText("");
        textqtyy.setText("");
        texttotal.setText("");
    }//GEN-LAST:event_btnaddActionPerformed

