    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        BasicDBObject indexupdate = new BasicDBObject();
        indexupdate.put("idPemesan", textidPemesan.getText());
        BasicDBObject update = new BasicDBObject();
        update.put("idPemesan", textidPemesan.getText());
        update.put("namaPegawai", textnamaPegawai.getText());
        update.put("namaCustomer", textnamaCustomer.getText());
        update.put("namaMakanan", textmakanan.getText());
        update.put("qty", textqty.getText());
        update.put("namaMinuman", textminuman.getText());
        update.put("qtyy", textqtyy.getText());
        update.put("totalHarga", texttotal.getText());
        
        tbl.setValueAt(textidPemesan.getText(), Baris, 0);
        tbl.setValueAt(textnamaPegawai.getText(), Baris, 1);
        tbl.setValueAt(textnamaCustomer.getText(), Baris, 2);
        tbl.setValueAt(textmakanan.getText(), Baris, 3);
        tbl.setValueAt(textqty.getText(), Baris, 4);
        tbl.setValueAt(textminuman.getText(), Baris, 5);
        tbl.setValueAt(textqtyy.getText(), Baris, 6);
        tbl.setValueAt(texttotal.getText(), Baris, 7);
        reset();
        tbltransaksi.setModel(tbl);
        collection.update(indexupdate, update);
    }//GEN-LAST:event_btneditActionPerformed

